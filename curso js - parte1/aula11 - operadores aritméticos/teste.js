//utilizando o parseInt para mudar uma string para number(Utilizado apenas para números inteiros);

const num = 10;
const num2 = parseInt("5")

console.log(num + num2);

// Utilizado parseFloat para mudadr de uma string para number(Utilizado para número inteiros e decimais);

const num3 = 10;
const num4 = parseFloat("5.2");

console.log(num3 + num4);

// Utilizando Number para mudar de uima string para um number (Utilizado para números inteiros e decimais)

const num5 = 10;
const num6 = Number("10.2");
const num7 = Number("5");

console.log(num5 + num6 + num7);